package _01_Array;

// Array in java is a collection of similar data types


public class _00_Array {
    public static void main(String[] args) {
      System.out.println("Array in Java");
      System.out.println( "Definition: An array is a collection of similar data types that are stored in contiguous memory locations.");
      System.out.println("An array is a data structure that allows you to store multiple values of the same type in a single variable.");
      System.out.println("Arrays are used to store collections of data, such as a list of numbers, names, or any other type of data.");
      System.out.println("In Java, arrays are objects that can hold a fixed number of values of a specific type.");
      System.out.println("You can think of an array as a container that holds a fixed number of items, all of the same type.");
      System.out.println("For example, if you want to store a list of integers, you can create an array of integers.");
      System.out.println("Here is a simple explanation of arrays in Java:");
      System.out.println("1. An array is a collection of similar data types.");
      System.out.println("2. It is a data structure that can hold multiple values of the same type.");
      System.out.println("3. Arrays are fixed in size, meaning once they are created, their size cannot be changed.");
      System.out.println("4. You can access elements in an array using their index, which starts from 0.");
      System.out.println("5. For example, if you have an array of integers, you can store and access multiple integer values using that array.");
      System.out.println("6. Arrays can be of any data type, including primitive types like int, char, and double, as well as reference types like String and custom objects.");
      System.out.println("7. You can create an array using the syntax: dataType[] arrayName = new dataType[size];");
      System.out.println("8. You can also initialize an array with values at the time of declaration using curly braces, like this: dataType[] arrayName = {value1, value2, value3};");
      System.out.println("9. Arrays can be multidimensional, meaning you can have arrays of arrays, such as a 2D array (matrix).");
      System.out.println("10. You can loop through an array using a for loop or enhanced for loop to access each element.");
      System.out.println("11. Arrays are useful for storing collections of data that you want to access and manipulate efficiently.");
      System.out.println("12. Remember that arrays in Java are zero-indexed, meaning the first element is at index 0, the second at index 1, and so on.");
      System.out.println("13. If you try to access an index that is out of bounds (greater than or equal to the size of the array), it will throw an ArrayIndexOutOfBoundsException.");
      System.out.println("14. You can find the length of an array using the length property, like this: arrayName.length.");
      System.out.println("15. Arrays can be used in various applications, such as storing lists of items, managing collections of data, and implementing algorithms that require sequential access to elements.");

      System.out.println("Here is a simple example of an array in Java:");
      System.out.println("int[] numbers = new int[5]; // This creates an array of integers with a size of 5");
      System.out.println("You can then assign values to the array like this:");
      System.out.println("numbers[0] = 10; // Assigns 10 to the first element of the array");
      System.out.println("You can also loop through the array to access all its elements:");
      System.out.println("for (int i = 0; i < numbers.length; i++) {");
      System.out.println("    System.out.println(numbers[i]); // Prints each element of the array");
      System.out.println("}");
      System.out.println("This is a basic overview of arrays in Java. They are useful for storing collections of data that you want to access and manipulate efficiently.");

        
    }
    
}
